void printPlayer(int16_t x, int16_t y, int16_t lastY, char forceRedraw, enum Form form) {

    if (y != lastY || forceRedraw == '1') {

        clearPlayer(x, lastY);

        switch (form) {
            case _normal:
                printNormal(x,y);
                break;
            case _sorcerer:
               printSorcerer(x, y);
                break;
            case _ranger:
                printRanger(x, y);
                break;
            case _knight:
                printKnight(x, y);
                break;
        };

    }
}

